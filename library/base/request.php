class bv48v_request extends bv48v_base {
/*****************************************************************************************
* ??document??
*****************************************************************************************/
	public function is_post($form=null)
	{
		$return = false;
		if ($_SERVER ['REQUEST_METHOD'] == 'POST')
		{
			$return = true;
			if(null!==$form)
			{
				$return = isset($_POST['submit'][$this->application()->slug][$form]);
			}
		}
		return $return;
	}
}